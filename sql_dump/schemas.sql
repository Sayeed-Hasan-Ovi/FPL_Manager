-- Function structure for CALCULATE_TOTAL_POINTS
-- ----------------------------
CREATE OR REPLACE
PROCEDURE "C##FPL_MANAGER"."CALCULATE_TOTAL_POINTS" AS
BEGIN
	total:=0;
	SELECT GOAL_SCORED INTO gs FROM PLAYER_STAT WHERE P_ID=pid AND FIXTURE_ID=fid;
	SELECT ASSISTS INTO ass FROM PLAYER_STAT WHERE P_ID=pid AND FIXTURE_ID=fid;
	SELECT GOAL_CONCEDED INTO gc FROM PLAYER_STAT WHERE P_ID=pid AND FIXTURE_ID=fid;
	SELECT OWN_GOAL INTO og FROM PLAYER_STAT WHERE P_ID=pid AND FIXTURE_ID=fid;
	SELECT PENALTY_MISSED INTO pm FROM PLAYER_STAT WHERE P_ID=pid AND FIXTURE_ID=fid;
	SELECT PENALTY_SAVED INTO ps FROM PLAYER_STAT WHERE P_ID=pid AND FIXTURE_ID=fid;
	SELECT RED_CARD INTO rc FROM PLAYER_STAT WHERE P_ID=pid AND FIXTURE_ID=fid;
	SELECT YELLOW_CARD INTO yc FROM PLAYER_STAT WHERE P_ID=pid AND FIXTURE_ID=fid;
	SELECT CLEAN_SHEET INTO cs FROM PLAYER_STAT WHERE P_ID=pid AND FIXTURE_ID=fid;
	SELECT POSITION INTO posn FROM PLAYER WHERE ID=pid;
	IF posn='FWD' THEN
		total:=(gs*4)+(ass*2)+(og*-2)+(pm*-2)+(rc*-2)+(yc*-1);
	ELSIF posn='MID' THEN
		total:=(gs*5)+(ass*3)+(og*-2)+(pm*-2)+(rc*-2)+(yc*-1);
	ELSIF posn='DEF' THEN
		total:=(gs*6)+(ass*4)+(og*-2)+(pm*-2)+(rc*-2)+(yc*-1)+TRUNC(gc*0.5)+(cs*4);
	ELSE
		total:=(gs*6)+(ass*4)+(og*-2)+(pm*-2)+(rc*-2)+(yc*-1)+TRUNC(gc*0.5,0)+(cs*4)+(ps*2);
	END IF;
	DBMS_output.put_line(total);
	UPDATE PLAYER_STAT SET TOTAL_POINTS=total WHERE P_ID=pid AND FIXTURE_ID=fid;
END;
